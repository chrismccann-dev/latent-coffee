export type Tier = 1 | 2 | 3

export type ReferenceRole = 'Anchor' | 'Signal' | 'Experimental'

export type ProducerSystem =
  | 'Colombia Processing Labs'
  | 'Panama Genetic + Precision Estates'
  | 'Ethiopia High-Altitude Clarity'
  | 'Peru Emerging Precision Layer'
  | 'China Experimental Processing'
  | 'Ecuador Mejorado + Precision Estates'
  | 'Thailand Experimental Processing'

