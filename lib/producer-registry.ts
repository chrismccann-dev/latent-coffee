export const PRODUCER_NAMES: readonly string[] = PRODUCERS.map((p) => p.name)

export const PRODUCER_LOOKUP = makeCanonicalLookup(PRODUCER_NAMES, PRODUCER_ALIASES)

// Back-compat exports preserved from the previous flat-string registry.
export const PRODUCER_REGISTRY = PRODUCER_NAMES
export const isCanonicalProducer = PRODUCER_LOOKUP.isCanonical
export const findClosestProducer = PRODUCER_LOOKUP.findClosest

// Lookup by canonical name OR alias. Returns the rich entry, or null.
export function getProducerEntry(name: string | null | undefined): ProducerEntry | null {
  if (!name) return null
  const canonical = PRODUCER_LOOKUP.canonicalize(name)
  if (!canonical) return null
  return PRODUCERS.find((p) => p.name === canonical) ?? null
}

// Tight-UI display name (currently same as canonical; reserved for future
// short-form variants if a producer becomes too long for cards).
export function getDisplayName(name: string | null | undefined): string {
  if (!name) return ''
  const e = getProducerEntry(name)
  return e?.displayName ?? e?.name ?? name
}
