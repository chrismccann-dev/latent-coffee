const NAME_TO_ENTRY: Map<string, RoasterEntry> = new Map(ROASTERS.map((r) => [r.name, r]))

export function getRoasterEntry(name: string | null | undefined): RoasterEntry | undefined {
  if (!name) return undefined
  return NAME_TO_ENTRY.get(name)
}

export function getDisplayName(name: string | null | undefined): string | null {
  if (!name) return null
  const entry = NAME_TO_ENTRY.get(name)
  return entry?.displayName ?? entry?.name ?? name
}

export function getRoasterFamily(name: string | null | undefined): RoasterFamily | 'Unknown' {
  if (!name) return 'Unknown'
  const entry = NAME_TO_ENTRY.get(name)
  if (!entry) return 'Unknown'
  return familyForStrategyTag(entry.strategyTag)
}

