export interface RoasterMetadata {
  fullName?: string
  location?: string
  url?: string
  bmrStrategy?: string
  bmrHouseStyle?: string
  notes?: string
}

export const ROASTER_METADATA: Record<string, RoasterMetadata> = Object.fromEntries(
  ROASTERS.map((r) => {
    const locationStr = r.location && r.country && r.location !== r.country
      ? `${r.location}, ${r.country}`
      : r.location ?? r.country
    return [
      r.name,
      {
        fullName: r.name,
        location: locationStr,
        url: r.url ?? r.brewGuideLink,
        bmrStrategy: r.strategyTag,
        bmrHouseStyle: r.bmrHouseStyle ?? r.houseStyle,
        notes: r.bmrNotes ?? r.notes,
      },
    ]
  }),
)

export function getRoasterMetadata(name: string | null | undefined): RoasterMetadata | undefined {
  if (!name) return undefined
  return ROASTER_METADATA[name]
}
